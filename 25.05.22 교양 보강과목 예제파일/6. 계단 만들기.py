import turtle  # turtle 그래픽 모듈 불러오기 (거북이 그래픽, 선 그리기 등)

# turtle 객체 생성 (거북이 하나 생성)
t = turtle.Turtle()

# 커서 모양을 거북이 모양으로 설정 (기본은 삼각형이지만 실제 거북이 모양으로 보임)
t.shape("turtle")

# 선 두께를 3으로 설정 (조금 굵은 선이 보이도록)
t.pensize(3)

# 계단처럼 오른쪽→위쪽 방향으로 10번 반복해서 이동
for _ in range(10):
    t.forward(30)   # 현재 방향(처음엔 오른쪽)으로 30픽셀 직선 이동
    t.left(90)      # 왼쪽으로 90도 회전 (이제 위쪽을 바라봄)
    t.forward(30)   # 위쪽으로 30픽셀 이동 (세로 부분 계단)
    t.right(90)     # 오른쪽으로 90도 회전해서 다시 원래 방향 복귀 (다음 계단 준비)

# turtle 그래픽 창을 닫지 않고 유지 (종료될 때까지 기다림)
turtle.done()
